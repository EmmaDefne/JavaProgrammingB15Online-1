public class WarmUpTask {
    public static void main(String[] args) {

        int start;
        int end = 27;


        for (start=10; start<=end; start++){
            System.out.print(start);



            //count 1-10
            for (int i=1 ; i<=10; i++){
                System.out.println("count 1 to 10 " +i);
            }
        }
    }
}
